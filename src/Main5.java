public class Main5 {
public static char[] toLowerCase(char[] arr){
    // Метод для преобразования всех букв в нижний регистр
        char[] result = new char[arr.length]; // Создаем новый массив для результата

        for (int i = 0; i < arr.length; i++) {
            char ch = arr[i];
            // Проверка: если символ заглавная буква, преобразуем его в строчную
            if (ch >= 'A' && ch <= 'Z') {
                result[i] = (char) (ch + 32); // Разница между 'A' и 'a' составляет 32
            } else {
                result[i] = ch; // Оставляем символ без изменений
            }
        }
        return result; // Возвращаем преобразованный массив
    }

    public static void main(String[] args) {
        // Пример использования метода
        char[] input = {'H', 'E', 'L', 'L', 'O', '!'};
        char[] output = toLowerCase(input);

        // Вывод результата
        System.out.println("Исходный массив: " + new String(input));
        System.out.println("Массив в нижнем регистре: " + new String(output));
    }
}



//Описание:
//Метод возвращает новый массив символов, где все буквы преобразованы в нижний регистр.
//Для каждой буквы в массиве проверяет, является ли она заглавной (например, A-Z).
// Если да, то заменяет её на соответствующую строчную (например, a-z).
//Если символ не является буквой, он остается неизменным.

